package main

// ISA micro-suite generator. Emits one .wat module per instruction family, each
// with one exported function per primitive that isolates that single opcode in a
// tight loop. The goal is a like-for-like per-opcode latency comparison across
// engines (wago / wazero / WARP) so base-level codegen deficiencies show up.
//
// Anti-optimization design: every compute primitive runs as a COUPLED
// DUAL-ACCUMULATOR dependent chain —
//     a = a OP b ; b = b OP a   (repeated K times, then n--)
// Each step consumes the previous step's result, so there is no independent
// parallelism to hide latency, no common subexpression to fold, no constant to
// propagate, and the final (a XOR b) depends on the runtime trip count n so the
// whole loop can't be eliminated. Operands are registers (the general case that
// stresses allocation), not immediates. The per-op cost is (measured ns) / (K*n),
// but since K and n are identical across a family the raw ns/op is directly
// comparable between opcodes and between engines.

import (
	"fmt"
	"strings"
)

// isaUnroll is the number of chained op-pairs per loop iteration; it amortizes
// the loop's br_if/decrement overhead so the body reflects the measured opcode.
const isaUnroll = 16

// isaModule is one generated .wat file plus the exports the manifest should time.
type isaModule struct {
	file    string   // e.g. "isa_i32.wasm"
	wat     string   // full module source
	exports []string // exported function names (one per opcode)
	arg     int      // loop trip count passed as the single i32 arg
}

// generateISA returns every ISA micro module.
func generateISA() []isaModule {
	return []isaModule{
		intModule("isa_i32", "i32", 2048),
		intModule("isa_i64", "i64", 2048),
		floatModule("isa_f32", "f32", 2048),
		floatModule("isa_f64", "f64", 2048),
		memModule(1024),
		bulkMemModule(256),
		ctlModule(2048),
		callModule(2048),
		varModule(2048),
		cvtModule(2048),
	}
}

// --- shared loop scaffold ---------------------------------------------------

// loopFn wraps a body (the unrolled chain, already indented) in the standard
// countdown loop and function shell. resultTy is the function's result type and
// resultExpr the returned value.
func loopFn(name, resultTy, locals, seed, body, resultExpr string) string {
	var b strings.Builder
	fmt.Fprintf(&b, "  (func (export %q) (param $n i32) (result %s)\n", name, resultTy)
	b.WriteString(locals)
	b.WriteString(seed)
	b.WriteString("    (block $brk (loop $lp\n")
	b.WriteString("      (br_if $brk (i32.eqz (local.get $n)))\n")
	b.WriteString(body)
	b.WriteString("      (local.set $n (i32.sub (local.get $n) (i32.const 1)))\n")
	b.WriteString("      (br $lp)))\n")
	fmt.Fprintf(&b, "    %s)\n", resultExpr)
	return b.String()
}

// --- integer families (i32 / i64) -------------------------------------------

func intModule(file, ty string, arg int) isaModule {
	one := ty + ".const 1"
	// seed b from the runtime arg so the chain (and result) depend on it.
	seedB := "(local.get $n)"
	if ty == "i64" {
		seedB = "(i64.extend_i32_s (local.get $n))"
	}
	locals := fmt.Sprintf("    (local $a %s) (local $b %s)\n", ty, ty)
	seed := fmt.Sprintf("    (local.set $a (%s))\n    (local.set $b %s)\n", one, seedB)
	resultExpr := fmt.Sprintf("(%s.xor (local.get $a) (local.get $b))", ty)

	// plain binary opcodes: a = a OP b ; b = b OP a
	bin := []string{"add", "sub", "mul", "and", "or", "xor", "shl", "shr_s", "shr_u", "rotl", "rotr"}
	// division/remainder need a nonzero, non-(-1) divisor: (x & MAX)|1 is positive & odd.
	divRem := []string{"div_s", "div_u", "rem_s", "rem_u"}
	// unary bit-count: a = b - OP(a) ; b = a - OP(b)
	un := []string{"clz", "ctz", "popcnt"}

	maxMask := "i32.const 0x7fffffff"
	if ty == "i64" {
		maxMask = "i64.const 0x7fffffffffffffff"
	}
	safeDivisor := func(x string) string {
		return fmt.Sprintf("(%s.or (%s.and %s (%s)) (%s.const 1))", ty, ty, x, maxMask, ty)
	}

	var b strings.Builder
	fmt.Fprintf(&b, ";; GENERATED by corpus/gen — do not edit by hand.\n;; %s: per-opcode %s latency micro (coupled dual-accumulator chain).\n(module\n", file, ty)

	var exports []string
	emit := func(name, body string) {
		b.WriteString(loopFn(name, ty, locals, seed, body, resultExpr))
		exports = append(exports, name)
	}

	for _, op := range bin {
		full := ty + "." + op
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $a (%s (local.get $a) (local.get $b)))\n", full)
			fmt.Fprintf(&body, "      (local.set $b (%s (local.get $b) (local.get $a)))\n", full)
		}
		emit(op, body.String())
	}
	for _, op := range divRem {
		full := ty + "." + op
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $a (%s (local.get $a) %s))\n", full, safeDivisor("(local.get $b)"))
			fmt.Fprintf(&body, "      (local.set $b (%s (local.get $b) %s))\n", full, safeDivisor("(local.get $a)"))
		}
		emit(op, body.String())
	}
	for _, op := range un {
		full := ty + "." + op
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $a (%s.sub (local.get $b) (%s (local.get $a))))\n", ty, full)
			fmt.Fprintf(&body, "      (local.set $b (%s.sub (local.get $a) (%s (local.get $b))))\n", ty, full)
		}
		emit(op, body.String())
	}

	b.WriteString(")\n")
	return isaModule{file: file + ".wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- float families (f32 / f64) ---------------------------------------------

func floatModule(file, ty string, arg int) isaModule {
	locals := fmt.Sprintf("    (local $a %s) (local $b %s)\n", ty, ty)
	// seed a=1.0, b=convert(n) so the chain depends on the runtime arg.
	conv := "f32.convert_i32_s"
	if ty == "f64" {
		conv = "f64.convert_i32_s"
	}
	seed := fmt.Sprintf("    (local.set $a (%s.const 1))\n    (local.set $b (%s (local.get $n)))\n", ty, conv)
	resultExpr := fmt.Sprintf("(%s.add (local.get $a) (local.get $b))", ty)

	bin := []string{"add", "sub", "mul", "div", "min", "max"}
	un := []string{"sqrt", "abs", "neg", "ceil", "floor", "trunc", "nearest"}

	var b strings.Builder
	fmt.Fprintf(&b, ";; GENERATED by corpus/gen — do not edit by hand.\n;; %s: per-opcode %s latency micro (coupled dual-accumulator chain).\n(module\n", file, ty)

	var exports []string
	emit := func(name, body string) {
		b.WriteString(loopFn(name, ty, locals, seed, body, resultExpr))
		exports = append(exports, name)
	}

	for _, op := range bin {
		full := ty + "." + op
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $a (%s (local.get $a) (local.get $b)))\n", full)
			fmt.Fprintf(&body, "      (local.set $b (%s (local.get $b) (local.get $a)))\n", full)
		}
		emit(op, body.String())
	}
	for _, op := range un {
		full := ty + "." + op
		// sqrt needs a non-negative operand; abs it first.
		arg := "(local.get $a)"
		argB := "(local.get $b)"
		if op == "sqrt" {
			arg = fmt.Sprintf("(%s.abs (local.get $a))", ty)
			argB = fmt.Sprintf("(%s.abs (local.get $b))", ty)
		}
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $a (%s.sub (local.get $b) (%s %s)))\n", ty, full, arg)
			fmt.Fprintf(&body, "      (local.set $b (%s.sub (local.get $a) (%s %s)))\n", ty, full, argB)
		}
		emit(op, body.String())
	}

	b.WriteString(")\n")
	return isaModule{file: file + ".wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- memory -----------------------------------------------------------------

// memModule walks a power-of-two buffer with a masked pointer. Sequential (stride
// 4/8) stays cache-resident and measures load/store issue; strided (stride 64,
// one cache line) measures dependent-miss latency. 16 pages = 1 MiB buffer.
func memModule(arg int) isaModule {
	const pages = 16
	const bufBytes = pages * 65536
	const maskSeq = bufBytes - 1

	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_mem: linear-memory load/store micro (masked pointer walk).\n")
	fmt.Fprintf(&b, "(module\n  (memory %d)\n", pages)

	var exports []string

	// load: acc += TY.load(p) ; p = (p + stride) & mask
	load := func(name, wty, addExpr, loadExpr string, stride int) {
		locals := "    (local $acc " + wty + ") (local $p i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $acc (%s (local.get $acc) (%s (local.get $p))))\n", addExpr, loadExpr)
			fmt.Fprintf(&body, "      (local.set $p (i32.and (i32.add (local.get $p) (i32.const %d)) (i32.const %d)))\n", stride, maskSeq)
		}
		b.WriteString(loopFn(name, wty, locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, name)
	}
	load("load_i32", "i32", "i32.add", "i32.load", 4)
	load("load_i64", "i64", "i64.add", "i64.load", 8)
	load("load_f64", "f64", "f64.add", "f64.load", 8)
	load("load_i32_stride", "i32", "i32.add", "i32.load", 64)

	// store: acc += p ; TY.store(p, acc) ; p = (p + stride) & mask
	store := func(name, wty, storeExpr string, valExpr string, stride int) {
		locals := "    (local $acc " + wty + ") (local $p i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (%s (local.get $p) %s)\n", storeExpr, valExpr)
			fmt.Fprintf(&body, "      (local.set $p (i32.and (i32.add (local.get $p) (i32.const %d)) (i32.const %d)))\n", stride, maskSeq)
		}
		b.WriteString(loopFn(name, "i32", locals, "", body.String(), "(local.get $p)"))
		exports = append(exports, name)
	}
	// vary the stored value cheaply so it isn't a fixed store: acc bumped from p.
	store("store_i32", "i32", "i32.store", "(i32.add (local.get $p) (local.get $n))", 4)
	store("store_i64", "i64", "i64.store", "(i64.extend_i32_s (i32.add (local.get $p) (local.get $n)))", 8)
	store("store_i32_stride", "i32", "i32.store", "(i32.add (local.get $p) (local.get $n))", 64)

	b.WriteString(")\n")
	return isaModule{file: "isa_mem.wasm", wat: b.String(), exports: exports, arg: arg}
}

// bulkMemModule measures the standardized bulk-memory primitives at sizes that
// cover the small, medium, and large backend paths. Each exported call repeats
// one operation arg times so host-call overhead is amortized equally in wago
// and wazero. The backward cases use overlapping ranges with dst > src.
func bulkMemModule(arg int) isaModule {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_bulk_mem: bulk-memory copy/fill throughput at fixed sizes.\n")
	b.WriteString("(module\n  (memory 2)\n")

	var exports []string
	emit := func(name, op string, resultAddr int) {
		fmt.Fprintf(&b, "  (func (export %q) (param $n i32) (result i32)\n", name)
		b.WriteString("    (block $done (loop $loop\n")
		b.WriteString("      (br_if $done (i32.eqz (local.get $n)))\n")
		fmt.Fprintf(&b, "      %s\n", op)
		b.WriteString("      (local.set $n (i32.sub (local.get $n) (i32.const 1)))\n")
		b.WriteString("      (br $loop)))\n")
		fmt.Fprintf(&b, "    (i32.load8_u (i32.const %d)))\n", resultAddr)
		exports = append(exports, name)
	}

	for _, size := range []int{64, 256, 4096} {
		emit(fmt.Sprintf("copy_fwd_%d", size),
			fmt.Sprintf("(memory.copy (i32.const 8192) (i32.const 0) (i32.const %d))", size), 8192)
		emit(fmt.Sprintf("copy_back_%d", size),
			fmt.Sprintf("(memory.copy (i32.const 32784) (i32.const 32768) (i32.const %d))", size), 32784)
		emit(fmt.Sprintf("fill_%d", size),
			fmt.Sprintf("(memory.fill (i32.const 49152) (i32.const 165) (i32.const %d))", size), 49152)
	}

	b.WriteString(")\n")
	return isaModule{file: "isa_bulk_mem.wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- control flow -----------------------------------------------------------

func ctlModule(arg int) isaModule {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_ctl: branching / selection micro.\n")
	b.WriteString("(module\n")
	var exports []string

	// loop_baseline: empty countdown — pure loop/branch overhead (subtract from others).
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (i32.add (local.get $acc) (i32.const 1)))\n")
		}
		b.WriteString(loopFn("loop_baseline", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "loop_baseline")
	}

	// br_if_taken: an inner conditional branch that is taken ~half the time.
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (block $s\n")
			body.WriteString("        (br_if $s (i32.and (local.get $acc) (i32.const 1)))\n")
			body.WriteString("        (local.set $acc (i32.add (local.get $acc) (i32.const 3))))\n")
			body.WriteString("      (local.set $acc (i32.add (local.get $acc) (i32.const 1)))\n")
		}
		b.WriteString(loopFn("br_if", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "br_if")
	}

	// if_else: data-dependent two-way selection via structured if/else.
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (if (result i32) (i32.and (local.get $acc) (i32.const 1))\n")
			body.WriteString("        (then (i32.add (local.get $acc) (i32.const 7)))\n")
			body.WriteString("        (else (i32.sub (local.get $acc) (i32.const 3)))))\n")
		}
		b.WriteString(loopFn("if_else", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "if_else")
	}

	// br_table: 4-way multiway dispatch on acc&3.
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (block $out (result i32)\n")
			body.WriteString("        (block $c0 (block $c1 (block $c2 (block $c3\n")
			body.WriteString("          (br_table $c0 $c1 $c2 $c3 (i32.and (local.get $acc) (i32.const 3))))\n")
			body.WriteString("          (br $out (i32.add (local.get $acc) (i32.const 7))))\n")
			body.WriteString("          (br $out (i32.add (local.get $acc) (i32.const 5))))\n")
			body.WriteString("          (br $out (i32.add (local.get $acc) (i32.const 3))))\n")
			body.WriteString("        (i32.add (local.get $acc) (i32.const 1))))\n")
		}
		b.WriteString(loopFn("br_table", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "br_table")
	}

	// select: branchless data-dependent selection.
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (select\n")
			body.WriteString("        (i32.add (local.get $acc) (i32.const 7))\n")
			body.WriteString("        (i32.sub (local.get $acc) (i32.const 3))\n")
			body.WriteString("        (i32.and (local.get $acc) (i32.const 1))))\n")
		}
		b.WriteString(loopFn("select", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "select")
	}

	b.WriteString(")\n")
	return isaModule{file: "isa_ctl.wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- calls ------------------------------------------------------------------

func callModule(arg int) isaModule {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_call: direct + indirect call overhead micro.\n")
	b.WriteString("(module\n")
	b.WriteString("  (type $bin (func (param i32 i32) (result i32)))\n")
	b.WriteString("  (func $leaf (type $bin) (i32.add (local.get 0) (local.get 1)))\n")
	b.WriteString("  (table 1 funcref)\n")
	b.WriteString("  (elem (i32.const 0) $leaf)\n")
	var exports []string

	// direct: acc = leaf(acc, n)
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (call $leaf (local.get $acc) (local.get $n)))\n")
		}
		b.WriteString(loopFn("call_direct", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "call_direct")
	}
	// indirect: acc = table[0](acc, n)
	{
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $acc (call_indirect (type $bin) (local.get $acc) (local.get $n) (i32.const 0)))\n")
		}
		b.WriteString(loopFn("call_indirect", "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, "call_indirect")
	}

	b.WriteString(")\n")
	return isaModule{file: "isa_call.wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- locals / globals -------------------------------------------------------

func varModule(arg int) isaModule {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_var: local + global get/set micro.\n")
	b.WriteString("(module\n")
	b.WriteString("  (global $g (mut i32) (i32.const 0))\n")
	var exports []string

	// local_getset: churn several locals (get + set + tee).
	{
		locals := "    (local $a i32) (local $b i32) (local $c i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (local.set $a (i32.add (local.get $b) (local.get $n)))\n")
			body.WriteString("      (local.set $b (i32.add (local.get $c) (local.get $a)))\n")
			body.WriteString("      (local.set $c (local.tee $a (i32.add (local.get $a) (local.get $b))))\n")
		}
		b.WriteString(loopFn("local_getset", "i32", locals, "", body.String(), "(i32.add (i32.add (local.get $a) (local.get $b)) (local.get $c))"))
		exports = append(exports, "local_getset")
	}
	// global_getset: read-modify-write a mutable global.
	{
		locals := ""
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			body.WriteString("      (global.set $g (i32.add (global.get $g) (local.get $n)))\n")
		}
		b.WriteString(loopFn("global_getset", "i32", locals, "", body.String(), "(global.get $g)"))
		exports = append(exports, "global_getset")
	}

	b.WriteString(")\n")
	return isaModule{file: "isa_var.wasm", wat: b.String(), exports: exports, arg: arg}
}

// --- conversions ------------------------------------------------------------

func cvtModule(arg int) isaModule {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; isa_cvt: width/type conversion micro (round-trip chains).\n")
	b.WriteString("(module\n")
	var exports []string

	// Each conversion is exercised as a round-trip so the accumulator stays i32
	// and the chain is dependent: acc = to_i32( cvt( acc ) ), plus n injection.
	emit := func(name, expr string) {
		locals := "    (local $acc i32)\n"
		var body strings.Builder
		for i := 0; i < isaUnroll; i++ {
			fmt.Fprintf(&body, "      (local.set $acc (i32.add (local.get $n) %s))\n", expr)
		}
		b.WriteString(loopFn(name, "i32", locals, "", body.String(), "(local.get $acc)"))
		exports = append(exports, name)
	}

	acc := "(local.get $acc)"
	// i32<->i64
	emit("i64_extend_s", fmt.Sprintf("(i32.wrap_i64 (i64.extend_i32_s %s))", acc))
	emit("i64_extend_u", fmt.Sprintf("(i32.wrap_i64 (i64.extend_i32_u %s))", acc))
	// i32<->f64 (trunc_sat is non-trapping)
	emit("f64_convert_s", fmt.Sprintf("(i32.trunc_sat_f64_s (f64.convert_i32_s %s))", acc))
	emit("f64_convert_u", fmt.Sprintf("(i32.trunc_sat_f64_u (f64.convert_i32_u %s))", acc))
	// i32<->f32
	emit("f32_convert_s", fmt.Sprintf("(i32.trunc_sat_f32_s (f32.convert_i32_s %s))", acc))
	// f32<->f64
	emit("promote_demote", fmt.Sprintf("(i32.trunc_sat_f64_s (f64.promote_f32 (f32.demote_f64 (f64.convert_i32_s %s))))", acc))
	// reinterpret round-trips (bit casts, no value change)
	emit("reinterpret_f32", fmt.Sprintf("(i32.reinterpret_f32 (f32.reinterpret_i32 %s))", acc))
	emit("reinterpret_f64", fmt.Sprintf("(i32.wrap_i64 (i64.reinterpret_f64 (f64.reinterpret_i64 (i64.extend_i32_s %s))))", acc))

	b.WriteString(")\n")
	return isaModule{file: "isa_cvt.wasm", wat: b.String(), exports: exports, arg: arg}
}
