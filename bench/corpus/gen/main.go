// Command gen emits the synthetic, scaling corpus modules as .wat. These stress
// the decode/compile stages in ways the hand-written modules do not: a module
// with many small functions, and a module with one very large function body.
//
// Usage: go run ./corpus/gen -out corpus/src
package main

import (
	"flag"
	"fmt"
	"os"
	"strings"
)

func main() {
	out := flag.String("out", "src", "directory to write generated .wat into")
	funcs := flag.Int("funcs", 300, "number of leaf functions for many_funcs")
	body := flag.Int("body", 1500, "number of unrolled steps for big_func")
	flag.Parse()

	write(*out, "many_funcs.wat", manyFuncs(*funcs))
	write(*out, "big_func.wat", bigFunc(*body))
}

func write(dir, name, content string) {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		fail(err)
	}
	if err := os.WriteFile(dir+"/"+name, []byte(content), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("gen: wrote %s/%s (%d bytes)\n", dir, name, len(content))
}

// manyFuncs builds a module of n leaf functions f0..f(n-1) plus an exported
// "run" that sums a fixed spread of them — many functions to decode/compile,
// cheap to execute.
func manyFuncs(n int) string {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; many_funcs: " + itoa(n) + " small functions; stresses per-function decode/compile.\n")
	b.WriteString("(module\n")
	for i := 0; i < n; i++ {
		fmt.Fprintf(&b, "  (func $f%d (param i32) (result i32) (i32.add (local.get 0) (i32.const %d)))\n", i, i)
	}
	b.WriteString("  (func (export \"run\") (param i32) (result i32)\n")
	picks := []int{0, n / 2, n - 1}
	expr := ""
	for i := len(picks) - 1; i >= 0; i-- {
		call := fmt.Sprintf("(call $f%d (local.get 0))", picks[i])
		if expr == "" {
			expr = call
		} else {
			expr = fmt.Sprintf("(i32.add %s %s)", call, expr)
		}
	}
	b.WriteString("    " + expr + "))\n")
	return b.String()
}

// bigFunc builds one function whose body is `steps` unrolled mixing operations —
// a single large body to stress big-function decode and codegen/regalloc.
func bigFunc(steps int) string {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; big_func: one function with ~" + itoa(steps) + " unrolled steps; stresses large-body decode/compile.\n")
	b.WriteString("(module\n")
	b.WriteString("  (func (export \"run\") (param i32) (result i64)\n")
	b.WriteString("    (local i64) ;; 1:acc\n")
	b.WriteString("    (local.set 1 (i64.extend_i32_s (local.get 0)))\n")
	for i := 0; i < steps; i++ {
		c := 2654435761 + int64(i)*1099511628211
		fmt.Fprintf(&b, "    (local.set 1 (i64.mul (local.get 1) (i64.const %d)))\n", c)
		fmt.Fprintf(&b, "    (local.set 1 (i64.xor (local.get 1) (i64.shr_u (local.get 1) (i64.const 13))))\n")
		fmt.Fprintf(&b, "    (local.set 1 (i64.add (local.get 1) (i64.const %d)))\n", i+1)
	}
	b.WriteString("    (local.get 1)))\n")
	return b.String()
}

func itoa(n int) string { return fmt.Sprintf("%d", n) }

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}
