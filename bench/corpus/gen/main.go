// Command gen emits the synthetic, scaling corpus module as .wat: a module with
// many small functions, which stresses per-function decode/compile in a way the
// hand-written modules do not.
//
// Usage: go run ./corpus/gen -out corpus/src
package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

func main() {
	out := flag.String("out", "src", "directory to write generated .wat into")
	funcs := flag.Int("funcs", 300, "number of leaf functions for many_funcs")
	flag.Parse()

	write(*out, "many_funcs.wat", manyFuncs(*funcs))
	writeISA(*out)
}

// isaManifestPath is the standalone manifest for the ISA micro modules, loaded
// alongside manifest.json by the bench suite and benchpub.
const isaManifestPath = "corpus/isa-manifest.json"

// manifestModule mirrors the fields the corpus manifest carries for one module.
type manifestModule struct {
	File     string       `json:"file"`
	Category string       `json:"category"`
	Desc     string       `json:"desc"`
	Exec     []manifestEx `json:"exec"`
}

type manifestEx struct {
	Export string  `json:"export"`
	Args   []int32 `json:"args"`
}

// writeISA emits every ISA micro module's .wat into dir and a manifest listing
// their exec entries. The .wat are compiled to .wasm by corpus/build.sh.
func writeISA(dir string) {
	var mods []manifestModule
	for _, m := range generateISA() {
		name := strings.TrimSuffix(m.file, ".wasm")
		write(dir, name+".wat", m.wat)
		var ex []manifestEx
		for _, e := range m.exports {
			ex = append(ex, manifestEx{Export: e, Args: []int32{int32(m.arg)}})
		}
		mods = append(mods, manifestModule{
			File:     m.file,
			Category: "isa",
			Desc:     "ISA micro: per-opcode latency (" + name + ")",
			Exec:     ex,
		})
	}
	raw, err := json.MarshalIndent(map[string]any{
		"_comment": "GENERATED by corpus/gen — ISA micro-suite. One export per opcode, each a coupled dual-accumulator dependent chain isolating that single instruction. Loaded alongside manifest.json.",
		"modules":  mods,
	}, "", "  ")
	if err != nil {
		fail(err)
	}
	if err := os.WriteFile(isaManifestPath, append(raw, '\n'), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("gen: wrote %s (%d modules)\n", filepath.Clean(isaManifestPath), len(mods))
}

func write(dir, name, content string) {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		fail(err)
	}
	if err := os.WriteFile(dir+"/"+name, []byte(content), 0o644); err != nil {
		fail(err)
	}
	fmt.Printf("gen: wrote %s/%s (%d bytes)\n", dir, name, len(content))
}

// manyFuncs builds a module of n leaf functions f0..f(n-1) plus an exported
// "run" that sums a fixed spread of them — many functions to decode/compile,
// cheap to execute.
func manyFuncs(n int) string {
	var b strings.Builder
	b.WriteString(";; GENERATED by corpus/gen — do not edit by hand.\n")
	b.WriteString(";; many_funcs: " + itoa(n) + " small functions; stresses per-function decode/compile.\n")
	b.WriteString("(module\n")
	for i := 0; i < n; i++ {
		fmt.Fprintf(&b, "  (func $f%d (param i32) (result i32) (i32.add (local.get 0) (i32.const %d)))\n", i, i)
	}
	b.WriteString("  (func (export \"run\") (param i32) (result i32)\n")
	picks := []int{0, n / 2, n - 1}
	expr := ""
	for i := len(picks) - 1; i >= 0; i-- {
		call := fmt.Sprintf("(call $f%d (local.get 0))", picks[i])
		if expr == "" {
			expr = call
		} else {
			expr = fmt.Sprintf("(i32.add %s %s)", call, expr)
		}
	}
	b.WriteString("    " + expr + "))\n")
	return b.String()
}

func itoa(n int) string { return fmt.Sprintf("%d", n) }

func fail(err error) {
	fmt.Fprintln(os.Stderr, "gen:", err)
	os.Exit(1)
}
