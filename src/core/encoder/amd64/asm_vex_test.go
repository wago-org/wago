package amd64

import (
	"bytes"
	"testing"
)

// Golden VEX byte encodings, cross-checked against the AVX spec / a reference
// assembler (3-byte VEX form).
func TestVexEncoding(t *testing.T) {
	cases := []struct {
		name string
		emit func(a *Asm)
		want []byte
	}{
		{"vaddsd xmm0,xmm1,xmm2", func(a *Asm) { a.VFAdd(0, 1, 2, true) }, []byte{0xC4, 0xE1, 0x73, 0x58, 0xC2}},
		{"vmulsd xmm0,xmm1,xmm2", func(a *Asm) { a.VFMul(0, 1, 2, true) }, []byte{0xC4, 0xE1, 0x73, 0x59, 0xC2}},
		{"vsubss xmm0,xmm1,xmm2", func(a *Asm) { a.VFSub(0, 1, 2, false) }, []byte{0xC4, 0xE1, 0x72, 0x5C, 0xC2}},
		{"vaddsd xmm8,xmm1,xmm2", func(a *Asm) { a.VFAdd(8, 1, 2, true) }, []byte{0xC4, 0x61, 0x73, 0x58, 0xC2}},
		{"vaddsd xmm0,xmm1,xmm10", func(a *Asm) { a.VFAdd(0, 1, 10, true) }, []byte{0xC4, 0xC1, 0x73, 0x58, 0xC2}},
		{"vandpd xmm0,xmm1,xmm2", func(a *Asm) { a.VSseRRR(0b01, 0x54, 0, 1, 2) }, []byte{0xC4, 0xE1, 0x71, 0x54, 0xC2}},
		{"vxorps xmm3,xmm3,xmm5", func(a *Asm) { a.VSseRRR(0b00, 0x57, 3, 3, 5) }, []byte{0xC4, 0xE1, 0x60, 0x57, 0xDD}},
		{"vpaddb xmm0,xmm1,xmm2", func(a *Asm) { a.VPaddb(0, 1, 2) }, []byte{0xC4, 0xE1, 0x71, 0xFC, 0xC2}},
		{"vpaddb xmm8,xmm1,xmm2", func(a *Asm) { a.VPaddb(8, 1, 2) }, []byte{0xC4, 0x61, 0x71, 0xFC, 0xC2}},
		{"vpaddb xmm0,xmm1,xmm10", func(a *Asm) { a.VPaddb(0, 1, 10) }, []byte{0xC4, 0xC1, 0x71, 0xFC, 0xC2}},
		{"vpxor xmm3,xmm3,xmm5", func(a *Asm) { a.VPxor(3, 3, 5) }, []byte{0xC4, 0xE1, 0x61, 0xEF, 0xDD}},
		{"vpshufb xmm0,xmm1,xmm2", func(a *Asm) { a.VPshufb(0, 1, 2) }, []byte{0xC4, 0xE2, 0x71, 0x00, 0xC2}},
		{"vpcmpeqq xmm0,xmm1,xmm2", func(a *Asm) { a.VPcmpeqq(0, 1, 2) }, []byte{0xC4, 0xE2, 0x71, 0x29, 0xC2}},
		{"vpcmpeqq xmm8,xmm1,xmm2", func(a *Asm) { a.VPcmpeqq(8, 1, 2) }, []byte{0xC4, 0x62, 0x71, 0x29, 0xC2}},
		{"vpcmpeqq xmm0,xmm1,xmm10", func(a *Asm) { a.VPcmpeqq(0, 1, 10) }, []byte{0xC4, 0xC2, 0x71, 0x29, 0xC2}},
		{"vpblendw xmm0,xmm1,xmm2,0xaa", func(a *Asm) { a.VPblendw(0, 1, 2, 0xaa) }, []byte{0xC4, 0xE3, 0x71, 0x0E, 0xC2, 0xAA}},
		{"pshufd xmm8,xmm9,0x1b", func(a *Asm) { a.Pshufd(8, 9, 0x1b) }, []byte{0x66, 0x45, 0x0F, 0x70, 0xC1, 0x1B}},
		{"pshuflw xmm10,xmm11,0x00", func(a *Asm) { a.Pshuflw(10, 11, 0x00) }, []byte{0xF2, 0x45, 0x0F, 0x70, 0xD3, 0x00}},
		{"punpcklqdq xmm12,xmm13", func(a *Asm) { a.Punpcklqdq(12, 13) }, []byte{0x66, 0x45, 0x0F, 0x6C, 0xE5}},
		{"pextrb r9d,xmm10,0x0f", func(a *Asm) { a.Pextrb(R9, 10, 0x0f) }, []byte{0x66, 0x45, 0x0F, 0x3A, 0x14, 0xD1, 0x0F}},
		{"pextrw r10d,xmm11,0x07", func(a *Asm) { a.Pextrw(R10, 11, 0x07) }, []byte{0x66, 0x45, 0x0F, 0xC5, 0xD3, 0x07}},
		{"pextrd r11d,xmm12,0x03", func(a *Asm) { a.Pextrd(R11, 12, 0x03) }, []byte{0x66, 0x45, 0x0F, 0x3A, 0x16, 0xE3, 0x03}},
		{"pextrq r12,xmm13,0x01", func(a *Asm) { a.Pextrq(R12, 13, 0x01) }, []byte{0x66, 0x4D, 0x0F, 0x3A, 0x16, 0xEC, 0x01}},
		{"pinsrb xmm8,r9d,0x0f", func(a *Asm) { a.Pinsrb(8, R9, 0x0f) }, []byte{0x66, 0x45, 0x0F, 0x3A, 0x20, 0xC1, 0x0F}},
		{"pinsrw xmm9,r10d,0x07", func(a *Asm) { a.Pinsrw(9, R10, 0x07) }, []byte{0x66, 0x45, 0x0F, 0xC4, 0xCA, 0x07}},
		{"pinsrd xmm10,r11d,0x03", func(a *Asm) { a.Pinsrd(10, R11, 0x03) }, []byte{0x66, 0x45, 0x0F, 0x3A, 0x22, 0xD3, 0x03}},
		{"pinsrq xmm11,r12,0x01", func(a *Asm) { a.Pinsrq(11, R12, 0x01) }, []byte{0x66, 0x4D, 0x0F, 0x3A, 0x22, 0xDC, 0x01}},
		{"vmovdqu xmm0,[rsp+0x12345678]", func(a *Asm) { a.VMovdquLoadDisp(0, RSP, 0x12345678) }, []byte{0xC4, 0xE1, 0x7A, 0x6F, 0x84, 0x24, 0x78, 0x56, 0x34, 0x12}},
		{"vmovdqu [rsp+0x12345678],xmm0", func(a *Asm) { a.VMovdquStoreDisp(RSP, 0x12345678, 0) }, []byte{0xC4, 0xE1, 0x7A, 0x7F, 0x84, 0x24, 0x78, 0x56, 0x34, 0x12}},
		{"vmovdqu xmm9,[r12+0x20]", func(a *Asm) { a.VMovdquLoadDisp(9, R12, 0x20) }, []byte{0xC4, 0x41, 0x7A, 0x6F, 0x8C, 0x24, 0x20, 0x00, 0x00, 0x00}},
		{"vmovdqu [r12+0x20],xmm9", func(a *Asm) { a.VMovdquStoreDisp(R12, 0x20, 9) }, []byte{0xC4, 0x41, 0x7A, 0x7F, 0x8C, 0x24, 0x20, 0x00, 0x00, 0x00}},
		{"vmovdqu xmm10,[r8+r9+0x7f]", func(a *Asm) { a.VMovdquLoadIdx(10, R8, R9, 0x7f) }, []byte{0xC4, 0x01, 0x7A, 0x6F, 0x94, 0x08, 0x7F, 0x00, 0x00, 0x00}},
		{"vmovdqu [r8+r9+0x7f],xmm10", func(a *Asm) { a.VMovdquStoreIdx(R8, R9, 10, 0x7f) }, []byte{0xC4, 0x01, 0x7A, 0x7F, 0x94, 0x08, 0x7F, 0x00, 0x00, 0x00}},
		{"vpaddd xmm8,xmm1,[r12+0x20]", func(a *Asm) { a.VPadddMemDisp(8, 1, R12, 0x20) }, []byte{0xC4, 0x41, 0x71, 0xFE, 0x84, 0x24, 0x20, 0x00, 0x00, 0x00}},
		{"vpshufb xmm3,xmm4,[r8+r9+0x7f]", func(a *Asm) { a.VPshufbMemIdx(3, 4, R8, R9, 0x7f) }, []byte{0xC4, 0x82, 0x59, 0x00, 0x9C, 0x08, 0x7F, 0x00, 0x00, 0x00}},
	}
	for _, c := range cases {
		a := &Asm{}
		c.emit(a)
		if !bytes.Equal(a.B, c.want) {
			t.Errorf("%s: got % x want % x", c.name, a.B, c.want)
		}
	}
}
