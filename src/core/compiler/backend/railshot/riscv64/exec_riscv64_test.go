//go:build linux && riscv64

package riscv64

import (
	"syscall"
	"testing"
	"unsafe"

	"github.com/wago-org/wago/src/core/runtime/riscv64spike"
)

type execCase struct {
	name   string
	params int
	body   []byte
	a0, a1 uintptr
	want   uint32
}

func runCases(t *testing.T, cases []execCase) {
	t.Helper()
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			code, err := CompileBeachhead(tc.params, tc.body)
			if err != nil {
				t.Fatal(err)
			}
			mem, err := riscv64spike.MapExec(code)
			if err != nil {
				t.Fatal(err)
			}
			defer syscall.Munmap(mem)
			entry := uintptr(unsafe.Pointer(&mem[0]))
			if got := uint32(riscv64spike.Call2(entry, tc.a0, tc.a1)); got != tc.want {
				t.Fatalf("result = %d (%#x), want %d (%#x)", got, got, tc.want, tc.want)
			}
		})
	}
}

func TestBeachheadIntegerExec(t *testing.T) {
	runCases(t, []execCase{
		{"const", 0, []byte{0x00, 0x41, 0x2a, 0x0b}, 0, 0, 42},
		{"add", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x6a, 0x0b}, 40, 2, 42},
		{"sub", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x6b, 0x0b}, 100, 58, 42},
		{"mul", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x6c, 0x0b}, 6, 7, 42},
		{"and", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x71, 0x0b}, 0xf0, 0x3c, 0x30},
		{"or", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x72, 0x0b}, 0x30, 0x0c, 0x3c},
		{"xor", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x73, 0x0b}, 0x33, 0x0f, 0x3c},
		{"shl", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x74, 0x0b}, 1, 5, 32},
		{"shr-s", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x75, 0x0b}, 0xfffffff0, 2, 0xfffffffc},
		{"shr-u", 2, []byte{0x00, 0x20, 0x00, 0x20, 0x01, 0x76, 0x0b}, 0xfffffff0, 2, 0x3ffffffc},
	})
}

func TestBeachheadComparisonsExec(t *testing.T) {
	for _, tc := range []struct {
		name string
		op   byte
		a, b uint32
		want uint32
	}{
		{"eq", 0x46, 7, 7, 1}, {"ne", 0x47, 7, 8, 1},
		{"lt-s", 0x48, 0xffffffff, 1, 1}, {"lt-u", 0x49, 0xffffffff, 1, 0},
		{"gt-s", 0x4a, 7, 2, 1}, {"gt-u", 0x4b, 7, 2, 1},
		{"le-s", 0x4c, 2, 2, 1}, {"le-u", 0x4d, 2, 1, 0},
		{"ge-s", 0x4e, 2, 2, 1}, {"ge-u", 0x4f, 0xffffffff, 1, 1},
	} {
		body := []byte{0x00, 0x20, 0x00, 0x20, 0x01, tc.op, 0x0b}
		runCases(t, []execCase{{tc.name, 2, body, uintptr(tc.a), uintptr(tc.b), tc.want}})
	}
	runCases(t, []execCase{
		{"eqz-zero", 1, []byte{0x00, 0x20, 0x00, 0x45, 0x0b}, 0, 0, 1},
		{"eqz-nonzero", 1, []byte{0x00, 0x20, 0x00, 0x45, 0x0b}, 9, 0, 0},
	})
}

func TestBeachheadControlExec(t *testing.T) {
	ifElse := []byte{
		0x01, 0x01, 0x7f,
		0x20, 0x00, 0x04, 0x40,
		0x41, 0x07, 0x21, 0x01,
		0x05,
		0x41, 0x09, 0x21, 0x01,
		0x0b, 0x20, 0x01, 0x0b,
	}
	fib := []byte{
		0x01, 0x03, 0x7f,
		0x41, 0x00, 0x21, 0x01,
		0x41, 0x01, 0x21, 0x02,
		0x41, 0x00, 0x21, 0x03,
		0x02, 0x40, 0x03, 0x40,
		0x20, 0x03, 0x20, 0x00, 0x4e, 0x0d, 0x01,
		0x20, 0x01, 0x20, 0x02, 0x6a,
		0x20, 0x02, 0x21, 0x01, 0x21, 0x02,
		0x20, 0x03, 0x41, 0x01, 0x6a, 0x21, 0x03,
		0x0c, 0x00, 0x0b, 0x0b,
		0x20, 0x01, 0x0b,
	}
	runCases(t, []execCase{
		{"if-true", 1, ifElse, 1, 0, 7},
		{"if-false", 1, ifElse, 0, 0, 9},
		{"fib0", 1, fib, 0, 0, 0},
		{"fib1", 1, fib, 1, 0, 1},
		{"fib10", 1, fib, 10, 0, 55},
		{"fib20", 1, fib, 20, 0, 6765},
	})
}
